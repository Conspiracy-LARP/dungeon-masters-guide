"""Provenance: every published file traces back to the pack (NFR-003).

NFR-003 — "100% of every published surface traces to a source document in the pack; zero
hand-written pages" — is the mechanism by which SC-004 ("no two surfaces can disagree")
is claimed true **by construction** rather than by vigilance. Unverified, that central
architectural claim rests on convention: someone drops a hand-written `about.html` into
the output, it publishes, and the site now says something the pack does not.

This project has already been bitten twice by exactly this class of drift — the reading
order diverged across four prose copies, and a hand-written link broke across two
branches. Both were caught by luck. This is the check that replaces luck.

The allow-list below is deliberately **explicit and small**. A wildcard entry would make
this function return "ok" for everything, which is worse than no check at all because it
looks like one.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Final

from build.config import BuildConfig
from build.roles import Document, load_assets, load_documents

#: Generated artifacts that legitimately have no single pack document behind them.
#: Each is derived from the pack as a whole, and each is named by a contract.
#: Adding an entry here is a deliberate act: it asserts "the build generates this, and
#: here is which work package does". Do not add a glob.
ALLOWED_GENERATED_FILES: Final[frozenset[str]] = frozenset(
    {
        "index.html",  # FR-001 landing page — derived from the kit's pitch (WP03)
        "llms.txt",  # FR-005 machine index (WP05)
        "llms-full.txt",  # FR-006 every chapter concatenated (WP05)
        ".nojekyll",  # FR-012 stops Jekyll processing the output (WP03)
        "book.html",  # FR-003 single-file book (WP04)
        "dungeon-masters-guide.pdf",  # FR-004 the printable book (WP04)
        "robots.txt",  # generated by WP03's hook; points crawlers at the sitemap
        "sitemap.xml",  # MkDocs
        "sitemap.xml.gz",  # MkDocs
        "sitemap.xsl",  # browser-only stylesheet for sitemap.xml, shipped by WP03's hook
        "404.html",  # MkDocs
    }
)

#: Directory prefixes for machine-generated bulk output: the search index and the
#: theme's own CSS/JS/fonts. These are prefixes, not a wildcard over the output root —
#: a stray file at the top level, or under any other directory, still fails.
ALLOWED_GENERATED_PREFIXES: Final[tuple[str, ...]] = (
    "assets/",  # Material theme CSS/JS/fonts, and WP03's extra_css
    "search/",  # MkDocs' search index
    "stylesheets/",  # extra_css declared in mkdocs.yml
)


class ProvenanceError(RuntimeError):
    """The output contains a file that does not trace to the pack."""


@dataclass(frozen=True)
class Unaccounted:
    """One output file with no source."""

    relative_path: str

    def describe(self) -> str:
        return (
            f"{self.relative_path}: traces to no document in src/pack/ and is not a "
            "known generated artifact. NFR-003 forbids hand-authored published "
            "content: if this file is real, it must be derived from the pack; if it "
            "is a new generated artifact, add it to ALLOWED_GENERATED_FILES in "
            "src/build/provenance.py so the exception is recorded rather than assumed."
        )


def _expected_from_documents(documents: list[Document], assets: list[str]) -> set[str]:
    """Every output path that the pack legitimately produces.

    Two renderings per document, per the url-map contract:
      * `{base}<doc>.md`      — raw markdown, byte-identical (FR-007)
      * `{base}<chapter>/`    — the chapter as a page (FR-001), i.e. `<stem>/index.html`

    Plus the pack's assets, published under their own names. An asset belongs here rather
    than in :data:`ALLOWED_GENERATED_FILES` because it is neither generated nor an
    exception: it *is* the pack, copied out under an identity transform, exactly as the
    raw markdown is. Listing it as a generated artifact would record a falsehood in the
    one place whose entire job is knowing where published bytes came from.
    """
    expected: set[str] = set(assets)
    for document in documents:
        # Raw markdown, under its published name (start.md publishes as AGENTS.md).
        expected.add(document.published_name)
        # ...and under its source name. Both trace to the same pack document, which is
        # the question this check asks. Whether the bootstrap SHOULD also appear as
        # `start.md` is the URL map's business (WP03), not provenance's: a file that
        # came from the pack is not hand-authored content, and NFR-003 is about
        # hand-authored content.
        expected.add(document.filename)
        # MkDocs' directory-style page output.
        stem = Path(document.filename).stem
        expected.add(f"{stem}/index.html")
        expected.add(f"{stem}.html")
    return expected


def find_unaccounted(output_dir: Path, config: BuildConfig) -> list[Unaccounted]:
    """Every file in ``output_dir`` that traces to neither the pack nor the allow-list.

    Args:
        output_dir: a built output tree (the site directory).
        config: the declaration, from which the pack's documents are resolved.

    Returns:
        The unaccounted files, sorted. Empty means NFR-003 holds for this output.
    """
    if not output_dir.is_dir():
        raise ProvenanceError(f"No output directory at {output_dir}; nothing to verify.")

    documents = load_documents(config.pack_dir, config)
    expected = _expected_from_documents(documents, load_assets(config.pack_dir))

    unaccounted: list[Unaccounted] = []
    for path in sorted(output_dir.rglob("*")):
        if not path.is_file():
            continue
        relative = path.relative_to(output_dir).as_posix()
        if relative in expected:
            continue
        if relative in ALLOWED_GENERATED_FILES:
            continue
        if relative.startswith(ALLOWED_GENERATED_PREFIXES):
            continue
        unaccounted.append(Unaccounted(relative_path=relative))
    return unaccounted


def verify_provenance(output_dir: Path, config: BuildConfig) -> None:
    """Assert NFR-003 over a built output.

    Raises:
        ProvenanceError: naming every file that does not trace back to the pack.
    """
    unaccounted = find_unaccounted(output_dir, config)
    if unaccounted:
        raise ProvenanceError(
            f"{len(unaccounted)} published file(s) do not trace back to src/pack/ "
            "(NFR-003):\n" + "\n".join(f"  - {u.describe()}" for u in unaccounted)
        )
