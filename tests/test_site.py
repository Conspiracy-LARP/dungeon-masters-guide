"""The site, asserted against a real build of the real pack.

Unlike the other test modules, most of these tests build the actual site rather than a
miniature one. That is deliberate and it is the point of several of them: the failures
this work package is guarding against — a wide table that overflows a phone, a raw
markdown file Jekyll ate, a hard-coded hostname, `/AGENTS.md` being subtly wrong while
the site looks perfect — are all failures that only exist in the built output, against
the real content. A sample page would prove nothing about the tables in
`storytelling.md`.

The one thing to know before adding a test here: `mkdocs.config.load_config` takes
overrides, so a test can build the real site with a different `site_url` without writing
a second config or touching the repo. That is what makes the base-swap assertion (C3,
NFR-001, SC-006) a real test rather than a grep.
"""

from __future__ import annotations

import importlib.util
import re
import subprocess
from pathlib import Path
from typing import Any, Final, Iterator

import pytest
import yaml
from mkdocs.commands.build import build as mkdocs_build
from mkdocs.config import load_config as mkdocs_load_config
from mkdocs.exceptions import Abort

from build.config import BuildConfig, load_config
from build.rename import PUBLISHED_NAME, SOURCE_NAME
from build.roles import load_documents
from build.site import (
    NOJEKYLL_FILENAME,
    PDF_FILENAME,
    PITCH_MARKER,
    PITCH_SOURCE,
    SiteError,
    extract_pitch,
)

_REPO_ROOT: Final[Path] = Path(__file__).resolve().parents[1]
_MKDOCS_YML: Final[Path] = _REPO_ROOT / "mkdocs.yml"

#: A second base, deliberately unlike the first in every respect that matters: another
#: host, another subpath depth, and the custom-domain case (SC-006's actual scenario).
_OTHER_BASE: Final[str] = "https://guide.example.test/"

#: Text files in the output whose content is generated by us or by MkDocs. The raw `.md`
#: files are excluded from some scans because they are the pack's own bytes.
_TEXT_SUFFIXES: Final[tuple[str, ...]] = (".html", ".xml", ".json", ".txt", ".css", ".md")


def _build(site_dir: Path, *, site_url: str | None = None) -> None:
    """Build the real site into ``site_dir``, optionally with a different base."""
    overrides: dict[str, Any] = {"site_dir": str(site_dir)}
    if site_url is not None:
        overrides["site_url"] = site_url
    config = mkdocs_load_config(config_file=str(_MKDOCS_YML), strict=True, **overrides)
    mkdocs_build(config)


def _text_files(site_dir: Path, *, suffixes: tuple[str, ...] = _TEXT_SUFFIXES) -> Iterator[Path]:
    for path in sorted(site_dir.rglob("*")):
        if path.is_file() and path.suffix in suffixes:
            yield path


@pytest.fixture(scope="session")
def site(tmp_path_factory: pytest.TempPathFactory) -> Path:
    """The real site, built once with the configured base."""
    site_dir = tmp_path_factory.mktemp("site")
    _build(site_dir)
    return site_dir


@pytest.fixture(scope="session")
def swapped_site(tmp_path_factory: pytest.TempPathFactory) -> Path:
    """The real site, built with a different base and nothing else changed."""
    site_dir = tmp_path_factory.mktemp("site-swapped")
    _build(site_dir, site_url=_OTHER_BASE)
    return site_dir


@pytest.fixture(scope="session")
def config() -> BuildConfig:
    return load_config(_MKDOCS_YML)


