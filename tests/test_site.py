_PITCH_FIXTURE = (
    "# The Kit\n"
    "\n"
    "**What is this?** In a sentence: the thing.\n"
    "\n"
    "A second paragraph of pitch.\n"
    "\n"
    "---\n"
    "\n"
    "*Hand this to an LLM.*\n"
    "\n"
    "## 0. The mission\n"
    "\n"
    "Not the pitch.\n"
)


def test_the_pitch_is_extracted_verbatim() -> None:
    """The landing page is the kit's words, not a paraphrase of them (FR-001, NFR-003)."""
    pitch = extract_pitch(_PITCH_FIXTURE)
    assert pitch.startswith("**What is this?** In a sentence: the thing.")
    assert "A second paragraph of pitch." in pitch


def test_the_pitch_stops_at_the_thematic_break() -> None:
    """The section ends where the kit ends it — the rule below the opening."""
    pitch = extract_pitch(_PITCH_FIXTURE)
    assert "Hand this to an LLM" not in pitch
    assert "Not the pitch" not in pitch


def test_the_pitch_stops_at_the_next_heading() -> None:
    """A kit that loses its horizontal rule still yields a pitch, not the whole document."""
    pitch = extract_pitch("**What is this?** The thing.\n\n## 0. The mission\n\nNo.\n")
    assert pitch == "**What is this?** The thing."


def test_a_missing_pitch_fails_the_build_rather_than_publishing_an_empty_page() -> None:
    """The loud failure T013 asks for.

    A silent one would publish a blank front door, or — worse — invite someone to author
    a landing page by hand, which is the drift NFR-003 exists to prevent.
    """
    with pytest.raises(SiteError) as raised:
        extract_pitch("# The Kit\n\nNo pitch here at all.\n")
    assert PITCH_MARKER in str(raised.value)
    assert PITCH_SOURCE in str(raised.value)


def test_an_empty_pitch_fails_the_build() -> None:
    with pytest.raises(SiteError):
        extract_pitch("# The Kit\n\n**What is this?**\n\n---\n\n## Next\n")


def test_the_real_kit_still_carries_the_pitch_the_landing_page_needs(config: BuildConfig) -> None:
    """The extraction is only as good as its grip on the real document.

    If someone rewords the kit's opening, this fires here — where the message explains
    what to do — rather than in a mysteriously blank site.
    """
    source = (config.pack_dir / PITCH_SOURCE).read_text(encoding="utf-8")
    assert extract_pitch(source).startswith(PITCH_MARKER)


def _longest_plain_run(markdown: str) -> str:
    """The longest run of unformatted words in some markdown.

    Derived rather than hard-coded, so that this test keeps working when the kit's
    wording changes — which it will, and which is the entire reason the landing page is
    extracted rather than authored. A run with no markdown syntax and no line break in it
    survives rendering unchanged, so it can be looked for in the HTML.
    """
    runs: list[str] = re.findall(r"[A-Za-z][A-Za-z ,';]{30,}", markdown)
    assert runs, "no plain-text run to look for"
    return max(runs, key=len).strip()


def test_the_landing_page_is_the_kits_pitch(site: Path, config: BuildConfig) -> None:
    """FR-001: the first thing a human reads is the guide's own pitch, not a directory."""
    index = (site / "index.html").read_text(encoding="utf-8")
    source = (config.pack_dir / PITCH_SOURCE).read_text(encoding="utf-8")
    pitch = extract_pitch(source)

    assert _longest_plain_run(pitch) in index

    # ...and the pitch only. The landing page is the front door, not the whole kit.
    assert "## 0. The mission" not in index
    assert _longest_plain_run(source.split("## 0. The mission", 1)[1]) not in index


