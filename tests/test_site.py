def test_no_link_discards_the_subpath(site: Path, config: BuildConfig) -> None:
    """C-003/C2: the site is served from a subpath; `/AGENTS.md` 404s there.

    Note what this does NOT ban. MkDocs renders `404.html` with root-relative URLs on
    purpose — a 404 can be served in place of any path, so it cannot use relative ones —
    and those URLs are correct precisely because they carry the subpath. The failure is a
    root-relative link that DROPS the subpath, so that is what is asserted.
    """
    subpath = "/" + config.base_url.split("//", 1)[1].split("/", 1)[1]
    assert subpath == "/dungeon-masters-guide/", "sanity: the configured base is a subpath"

    offenders: list[str] = []
    for path in _text_files(site, suffixes=(".html",)):
        for match in re.finditer(r'(?:href|src)="(/[^/"][^"]*)"', path.read_text(encoding="utf-8")):
            if not match.group(1).startswith(subpath):
                offenders.append(f"{path.relative_to(site)}: {match.group(1)}")
    assert not offenders, "root-relative links that drop the subpath will 404:\n" + "\n".join(
        offenders
    )


