_TYPOGRAPHY: Final[Path] = _OVERRIDES / "stylesheets" / "typography.css"

#: Foreground/background pairs that carry text, with the scheme-local custom properties
#: they are defined by. Every one must clear WCAG AA (4.5:1) in both schemes (NFR-006).
CONTRAST_PAIRS: Final[tuple[tuple[str, str, str], ...]] = (
    ("--md-typeset-color", "--md-default-bg-color", "body text"),
    ("--md-typeset-a-color", "--md-default-bg-color", "links"),
    ("--md-default-fg-color--light", "--md-default-bg-color", "captions and metadata"),
    ("--md-default-fg-color--light", "--md-code-bg-color", "the AI pointer"),
    ("--md-accent-fg-color", "--md-default-bg-color", "links on hover"),
    ("--md-code-fg-color", "--md-code-bg-color", "code"),
    ("--md-primary-bg-color", "--md-primary-fg-color", "the header"),
    ("--md-footer-fg-color", "--md-footer-bg-color", "the footer"),
)

_AA_NORMAL_TEXT: Final[float] = 4.5

_DECLARATION: Final[re.Pattern[str]] = re.compile(
    r"^\s*(--[\w-]+)\s*:\s*(#[0-9a-fA-F]{6})\s*;", re.MULTILINE
)


def _relative_luminance(hex_colour: str) -> float:
    """WCAG 2.1 relative luminance of an sRGB colour."""
    channels = [int(hex_colour[i : i + 2], 16) / 255 for i in (1, 3, 5)]
    linear = [c / 12.92 if c <= 0.03928 else ((c + 0.055) / 1.055) ** 2.4 for c in channels]
    return 0.2126 * linear[0] + 0.7152 * linear[1] + 0.0722 * linear[2]


def contrast_ratio(foreground: str, background: str) -> float:
    """The WCAG contrast ratio between two `#rrggbb` colours."""
    lighter, darker = sorted(
        (_relative_luminance(foreground), _relative_luminance(background)), reverse=True
    )
    return (lighter + 0.05) / (darker + 0.05)


def _scheme(css: str, name: str) -> dict[str, str]:
    """The custom properties declared for one Material colour scheme."""
    block = re.search(rf'\[data-md-color-scheme="{name}"\]\s*\{{(.*?)\n\}}', css, re.DOTALL)
    assert block is not None, f"palette.css declares no {name!r} scheme"
    return {m.group(1): m.group(2) for m in _DECLARATION.finditer(block.group(1))}


@pytest.mark.parametrize("scheme", ["default", "slate"])
def test_the_palette_meets_wcag_aa(scheme: str) -> None:
    """NFR-006, measured rather than trusted to the eye — in light AND dark.

    The dark scheme is where this usually goes wrong: a colour picked against white and
    then reused on near-black looks fine to whoever picked it and fails for everyone else.
    """
    colours = _scheme(_PALETTE.read_text(encoding="utf-8"), scheme)
    for foreground, background, what in CONTRAST_PAIRS:
        assert foreground in colours, f"{scheme} declares no {foreground}"
        assert background in colours, f"{scheme} declares no {background}"
        ratio = contrast_ratio(colours[foreground], colours[background])
        assert ratio >= _AA_NORMAL_TEXT, (
            f"{scheme}: {what} is {ratio:.2f}:1 ({colours[foreground]} on "
            f"{colours[background]}), below WCAG AA's {_AA_NORMAL_TEXT}:1"
        )


