# --- Link validation (C2, FR-013) ------------------------------------------------------


def _validation_declaration() -> dict[str, Any]:
    """The `validation:` block from the real mkdocs.yml.

    Read from the config rather than restated, so these tests fail if the settings are
    weakened — which is the only way they could stop being true.
    """
    raw = yaml.safe_load(_MKDOCS_YML.read_text(encoding="utf-8").split("theme:", 1)[0])
    validation = raw["validation"]
    assert isinstance(validation, dict)
    return validation


def _synthetic_project(tmp_path: Path, body: str, *, validation: dict[str, Any] | None) -> Path:
    """A minimal project with one broken link, built with our validation settings."""
    docs = tmp_path / "docs"
    docs.mkdir()
    (docs / "index.md").write_text("# Home\n\n## A real anchor\n\n" + body, encoding="utf-8")

    config: dict[str, Any] = {
        "site_name": "Probe",
        "site_url": "https://example.test/probe/",
        "docs_dir": "docs",
        "site_dir": str(tmp_path / "site"),
        "nav": [{"Home": "index.md"}],
    }
    if validation is not None:
        config["validation"] = validation

    path = tmp_path / "mkdocs.yml"
    path.write_text(yaml.safe_dump(config, sort_keys=False), encoding="utf-8")
    return path


def _build_probe(config_path: Path) -> None:
    mkdocs_build(mkdocs_load_config(config_file=str(config_path), strict=True))


@pytest.mark.parametrize(
    ("body", "kind"),
    [
        ("[gone](no-such-document.md)\n", "a link to a document that does not exist"),
        ("[anchor](index.md#not-a-real-anchor)\n", "a link to an anchor that does not exist"),
        ("[odd](some/directory/)\n", "a link that cannot be an internal document"),
    ],
)
def test_a_broken_internal_link_fails_a_strict_build(tmp_path: Path, body: str, kind: str) -> None:
    """FR-013 on the site — the last published surface that was not covered (C2).

    Cross-references must resolve on *every* published surface. WP07 covers the two
    branches and WP04 covers the book's flattened references; this closes the site, by
    making MkDocs' own link validation loud enough for `--strict` to act on.
    """
    config_path = _synthetic_project(tmp_path, body, validation=_validation_declaration())
    with pytest.raises(Abort):
        _build_probe(config_path)


def test_the_link_validation_settings_are_what_makes_that_true(tmp_path: Path) -> None:
    """The anti-vacuity guard: prove the setting is load-bearing, not decorative.

    Same trap as `validation.nav.omitted_files`, which WP02 found: MkDocs defaults
    `anchors` to `info`, and `--strict` escalates only WARNING. So on the defaults, a
    link to an anchor that does not exist builds green — verified right here, so that
    nobody has to take the claim on trust, and so that a future "tidy-up" that drops the
    setting fails a test instead of quietly disabling FR-013 for the site.
    """
