def test_our_css_does_not_undo_the_containment() -> None:
    """The other half: Material contains the tables unless we get in the way.

    The measure cap in typography.css applies to flow elements only, and deliberately not
    to tables — capping their container would narrow the scroll area for no benefit, and
    a `min-width` or a fixed `px` width on a table is the classic way a page starts
    scrolling sideways on a phone.
    """
    css = _without_comments(
        _TYPOGRAPHY.read_text(encoding="utf-8") + _PALETTE.read_text(encoding="utf-8")
    )
    for rule in re.finditer(r"([^{}]+)\{([^}]*)\}", css):
        selector, body = rule.group(1), rule.group(2)
        if "table" not in selector:
            continue
        assert "min-width" not in body, f"{selector.strip()} sets a min-width on a table"
        assert "overflow" not in body, f"{selector.strip()} overrides Material's containment"

    assert "max-width: 72ch" in css, "the measure cap has gone"
    assert not re.search(r"width:\s*\d{3,}px", css), "a fixed pixel width wider than a phone"


def _without_comments(css: str) -> str:
    """CSS with its comments removed, so a selector match cannot swallow the prose.

    These stylesheets are heavily commented on purpose; a rule-matching regex that does
    not strip comments first matches the comment above the rule.
    """
    return re.sub(r"/\*.*?\*/", "", css, flags=re.DOTALL)


def _material_stylesheet() -> str:
    """Material's own compiled CSS — the thing our overrides have to win against.

    Every sheet, not just `main.*.min.css`. The two signals this file reads off Material
    live in different files: `--md-text-font-family` is declared in `main`, while all 50
    occurrences of `data-md-color-primary` — including the
    `[scheme][primary=indigo]{--md-typeset-a-color:#5488e8}` rule that `primary: custom`
    exists to escape — are in the sibling `palette.*.min.css`. Globbing `main` alone
    pointed the palette canary at a file that could never carry its signal, so it could
    not fail. Concatenating is what gives it teeth.
    """
    spec = importlib.util.find_spec("material")
    assert spec is not None and spec.origin is not None, "Material is not installed"
    sheets = sorted(
        (Path(spec.origin).parent / "templates" / "assets" / "stylesheets").glob("*.min.css")
    )
    assert sheets, "Material's stylesheets are not where they have always been"
    return "".join(sheet.read_text(encoding="utf-8") for sheet in sheets)


def test_the_font_stack_is_declared_where_material_can_see_it() -> None:
    """The silent no-op that shipped a font nobody chose, pinned down.

    Material declares `--md-text-font-family` **on `body`**, not on `:root`. So a
    `:root { --md-text-font-family: ... }` override sets the variable on `html` and is
    then shadowed at `body` by Material's own declaration: every element resolves
    Material's, the `var(--md-text-font, _)` fallback kicks in, and the site renders in
    the system sans-serif. Nothing warns. `getComputedStyle(html)` even reports our value
    back, because on `html` it genuinely is set — which is what makes this so easy to
    "verify" and still get wrong. It was caught by measuring a rendered page.

    Two things are asserted, and the second is the one with a shelf life:
      1. our declaration sits on `body`, at the same specificity, in a stylesheet that
         loads after Material's;
      2. Material still declares it on `body`. If a Material upgrade moves that
         declaration, this fails — and the fix is to re-check the rendered page, not to
         edit the assertion.
    """
    material_css = _material_stylesheet()
    declaring = re.search(r"([^{};]*)\{[^{}]*--md-text-font-family:", material_css)
    assert declaring is not None, "Material no longer declares --md-text-font-family at all"
    assert declaring.group(1).strip().endswith("body"), (
        f"Material now declares --md-text-font-family on {declaring.group(1)!r}, not on "
        "`body`. Our override in typography.css targets `body` precisely to beat it — "
        "re-check the rendered page's computed font-family before adjusting this."
    )

    ours = re.search(
        r"([^{}]*)\{([^{}]*--md-text-font-family:[^{}]*)\}",
        _without_comments(_TYPOGRAPHY.read_text(encoding="utf-8")),
    )
    assert ours is not None, "typography.css declares no font stack"
    assert ours.group(1).strip() == "body", (
        f"the font stack is declared on {ours.group(1).strip()!r}. Material declares it "
        "on `body`, which shadows anything set on `:root` — the site would silently "
        "render in the system sans-serif."
    )
    assert "--md-code-font-family" in ours.group(2), "the mono stack shares the same trap"


def test_the_theme_is_not_silently_git_ignored() -> None:
    """The third trap of this kind, and the one that would have shipped nothing at all.

    `.gitignore` carried an unanchored `site/` for the MkDocs output — which also matches
    `src/theme/site/`, this work package's whole authoritative surface. Git declines to
    add it and says nothing; the palette, the typography and the template override simply
    never leave the machine, and the build fails later and elsewhere, on a `custom_dir`
    that is not there. The pattern is now anchored to the repo root (`/site/`).

    `--no-index` is load-bearing, and its absence was this guard's own version of the bug
    it guards. `git check-ignore` consults the index by default and reports nothing for a
    tracked file — so once the theme was committed, the check passed no matter what the
    pattern said, and reverting `.gitignore` to the unanchored `site/` left it green.
    `--no-index` asks the question actually at stake: would the pattern refuse a *new*
    file here? That is the live failure — `src/theme/site/overrides/partials/foo.html`
    added tomorrow is still silently unaddable under an unanchored `site/`, and the
    existing files being tracked does nothing to save the next contributor.
    """
    tracked = [
        str(path.relative_to(_REPO_ROOT))
        for path in (_REPO_ROOT / "src" / "theme").rglob("*")
        if path.is_file()
    ]
    assert tracked, "sanity: the theme has files"

    ignored = subprocess.run(
        ["git", "check-ignore", "--no-index", *tracked],
        cwd=_REPO_ROOT,
        capture_output=True,
        text=True,
        check=False,
    )
    assert not ignored.stdout.strip(), (
        "git ignores part of the theme, so it cannot be committed and the site will "
        f"build without it:\n{ignored.stdout}"
    )


